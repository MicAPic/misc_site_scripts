function hide1() {
  let object = document.getElementById("c1"),
      object_tab = document.getElementById("t1"),
      x = document.getElementById("c2"),
      x_tab = document.getElementById("t2"),
      y = document.getElementById("c3"),
      y_tab = document.getElementById("t3"),
      z = document.getElementById("c4"),
      z_tab = document.getElementById("t4");
  if (object.style.display === "none") {
    x.style.display = "none";
    x_tab.style.color = "a0a0a0";
    x_tab.style.borderBottom = "4px solid transparent";
    y.style.display = "none";
    y_tab.style.color = "a0a0a0";
    y_tab.style.borderBottom = "4px solid transparent";
    z.style.display = "none";
    z_tab.style.color = "a0a0a0";
    z_tab.style.borderBottom = "4px solid transparent";
    object.setAttribute("style", "display: grid;\n" +
        "\tgap: 45px 183px;\n" +
        "\tgrid-template-columns: 405px 405px;\n" +
        "\tjustify-content: center;\n" +
        "\tmargin-top: -835px;");
    object_tab.style.color = "white";
    object_tab.style.borderBottom = "4px solid white";
  }
}

